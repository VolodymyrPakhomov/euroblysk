/** Знайти шаблон (по subcategory, потім group, потім category) */
function getTemplate(product) {
  return (
    TEMPLATES[product.subcategory] ||
    TEMPLATES[product.group] ||
    TEMPLATES[product.category] ||
    null
  );
}

/** Дефолтний шаблон якщо нічого не знайдено */
function defaultDesc(b, v, c) {
  return `${b}${vol(v)}${from(c)} — якісний товар для дому. Відмінне співвідношення ціни та якості.`;
}

// ── Основна логіка ────────────────────────────────────────────────────────────

const products = JSON.parse(readFileSync(PRODUCTS_PATH, "utf-8"));

let generated = 0;
let skipped = 0;

const updated = products.map(product => {
  if (product.description && product.description.trim()) {
    skipped++;
    return product;
  }

  const brand   = extractBrand(product.title);
  const volume  = extractVolume(product.title);
  const country = extractCountry(product.title);

  const tpl = getTemplate(product);
  const description = tpl
    ? tpl(brand, volume, country)
    : defaultDesc(brand, volume, country);

  generated++;
  return { ...product, description };
});

writeFileSync(PRODUCTS_PATH, JSON.stringify(updated, null, 2), "utf-8");
console.log(`✓ Готово: згенеровано ${generated}, пропущено ${skipped}`);
