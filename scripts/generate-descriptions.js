/** Витягти об'єм / кількість із назви: (500мл), (1л), (200г), (3кг), (10шт) */
function extractVolume(title) {
  const m = title.match(/\(([^)]+(?:мл|л|г|кг|шт|ст|таб|пак)[^)]*)\)/i);
  return m ? m[1] : null;
}

/** Витягти країну з emoji-прапора */
const FLAG_COUNTRY = {
  "🇩🇪": "Німеччина", "🇺🇦": "Україна", "🇧🇪": "Бельгія",
  "🇵🇱": "Польща",   "🇫🇷": "Франція", "🇮🇹": "Італія",
  "🇬🇧": "Велика Британія", "🇳🇱": "Нідерланди", "🇨🇿": "Чехія",
  "🇦🇹": "Австрія",  "🇨🇭": "Швейцарія", "🇸🇪": "Швеція",
  "🇪🇺": "Європа",   "🇺🇸": "США",
};
function extractCountry(title) {
  for (const [flag, name] of Object.entries(FLAG_COUNTRY)) {
    if (title.includes(flag)) return name;
  }
  return null;
}

/** Витягти бренд — перше слово (або два якщо обидва з великої) */
function extractBrand(title) {
  const words = title.trim().split(/\s+/);
  const first = words[0];
  if (!first) return "";
  // якщо друге слово теж велике і коротке — може бути частиною бренду
  if (words[1] && /^[A-ZА-ЯЇІЄҐ]/.test(words[1]) && words[1].length <= 3) {
    return first + " " + words[1];
  }
  return first;
}

/** Сформувати рядок з об'ємом */
function vol(v) { return v ? ` об'ємом ${v}` : ""; }
/** Сформувати рядок з країною */
function from(c) { return c ? ` (${c})` : ""; }

